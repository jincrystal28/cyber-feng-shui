    """
    赛博堪舆底层算理引擎：提供八宅法与九宫飞星算法
    """
    
    # ==========================================
    # 算法一：八宅明镜（根据房屋坐向定八方吉凶）
    # ==========================================
    @staticmethod
    def calculate_bazhai(sitting_direction):
        """
        计算八宅吉凶方位
        :param sitting_direction: 房屋坐方 (坎/艮/震/巽/离/坤/兑/乾)
        :return: 包含八个方位吉凶的字典
        """
        # 四吉星：生气(大吉)、延年(中吉)、天医(中吉)、伏位(小吉)
        # 四凶星：绝命(大凶)、五鬼(大凶)、六煞(中凶)、祸害(小凶)
        
        bazhai_map = {
            "坎": {"正北": "伏位(吉)", "正南": "延年(吉)", "正东": "天医(吉)", "东南": "生气(吉)", "正西": "祸害(凶)", "西北": "六煞(凶)", "东北": "五鬼(凶)", "西南": "绝命(凶)"},
            "震": {"正东": "伏位(吉)", "正南": "生气(吉)", "正北": "天医(吉)", "东南": "延年(吉)", "正西": "绝命(凶)", "西北": "五鬼(凶)", "东北": "六煞(凶)", "西南": "祸害(凶)"},
            "巽": {"东南": "伏位(吉)", "正南": "天医(吉)", "正北": "生气(吉)", "正东": "延年(吉)", "正西": "六煞(凶)", "西北": "祸害(凶)", "东北": "绝命(凶)", "西南": "五鬼(凶)"},
            "离": {"正南": "伏位(吉)", "正北": "延年(吉)", "正东": "生气(吉)", "东南": "天医(吉)", "正西": "五鬼(凶)", "西北": "绝命(凶)", "东北": "祸害(凶)", "西南": "六煞(凶)"},
            "乾": {"西北": "伏位(吉)", "正西": "生气(吉)", "东北": "天医(吉)", "西南": "延年(吉)", "正南": "绝命(凶)", "正北": "六煞(凶)", "正东": "五鬼(凶)", "东南": "祸害(凶)"},
            "坤": {"西南": "伏位(吉)", "正西": "天医(吉)", "西北": "延年(吉)", "东北": "生气(吉)", "正南": "六煞(凶)", "正北": "绝命(凶)", "正东": "祸害(凶)", "东南": "五鬼(凶)"},
            "兑": {"正西": "伏位(吉)", "西北": "生气(吉)", "东北": "延年(吉)", "西南": "天医(吉)", "正南": "五鬼(凶)", "正北": "祸害(凶)", "正东": "绝命(凶)", "东南": "六煞(凶)"},
            "艮": {"东北": "伏位(吉)", "正西": "延年(吉)", "西北": "天医(吉)", "西南": "生气(吉)", "正南": "祸害(凶)", "正北": "五鬼(凶)", "正东": "六煞(凶)", "东南": "绝命(凶)"}
        }
        return bazhai_map.get(sitting_direction, {})

    # ==========================================
    # 算法二：流年九宫飞星（计算当年的风水气场分布）
    # ==========================================
    @staticmethod
    def calculate_flying_stars(year=None):
        """
        计算指定年份的九宫飞星分布（默认当前年份）
        基于洛书轨迹：中5 -> 西北6 -> 西7 -> 东北8 -> 南9 -> 北1 -> 西南2 -> 东3 -> 东南4
        """
        if year is None:
            year = datetime.datetime.now().year
            
        # 1. 计算流年入中宫的星 (公式: 11 - (年份每位数字相加至个位))
        # 简便算法: 11 - (year % 9), 若余数为0则视为9
        remainder = year % 9
        if remainder == 0:
            remainder = 9
        center_star = 11 - remainder
        if center_star > 9:
            center_star -= 9
            
        # 洛书九宫飞星固定轨迹 (相对于中宫星的增量顺序)
        # 例如：中宫填入星N，西北就是N+1，正西就是N+2...
        luoshu_path = ["中宫", "西北", "正西", "东北", "正南", "正北", "西南", "正东", "东南"]
        
        # 九星在目前(九运: 2024-2043)的吉凶属性
        star_meanings = {
            1: "一白贪狼星 (吉/偏财/桃花)",
            2: "二黑巨门星 (大凶/病符星)",
            3: "三碧禄存星 (凶/是非/官非)",
            4: "四绿文曲星 (半吉/文昌/学业)",
            5: "五黄廉贞星 (极凶/大灾大难)",
            6: "六白武曲星 (吉/偏财/贵人)",
            7: "七赤破军星 (凶/破财/退气)",
            8: "八白左辅星 (半吉/退气正财)",
            9: "九紫右弼星 (大吉/九运当旺财星)"
        }
        
        # 排盘布阵
        annual_stars = {}
        current_star = center_star
        
        for pos in luoshu_path:
            annual_stars[pos] = f"[{current_star}]{star_meanings[current_star]}"
            current_star += 1
            if current_star > 9:
                current_star -= 9 # 九星循环
                
        return {"year": year, "stars": annual_stars}
